# Lookup table: tc_id → row dict
CSV_ROWS = {r["tc_id"]: r for r in load_csv("FR_01_user_registration.csv")}


def _tc(tc_id):
    return CSV_ROWS[tc_id]


def _unique_email(domain="example.com"):
    return f"{settings.TEST_EMAIL_PREFIX}{uuid.uuid4().hex[:10]}@{domain}"


def _expected_msg(tc):
    """Prefer the parsed quoted message; fall back to the full expected text."""
    return tc["expected_messages"] or tc["expected"]


