class TestSecurity:
    def test_FR_001_027_xss_in_fullname(
        self, driver, db_conn, redis_conn, cleanup_test_users
    ):
        """Script in fullName must NOT execute as JS — React escapes by default."""
        tc = _tc("FR_001_027")
        email = _unique_email()
        page = RegisterPage(driver).open()
        page.fill_form(
            email=email,
            fullName=tc["fullname"],  # "<script>alert(1)</script>"
            phone="",
            password=tc["password"],
            confirmPassword=tc["password"],
        )
        page.click_submit()
        # No alert dialog should appear at any point
        time.sleep(1)
        try:
            alert = driver.switch_to.alert
            alert.dismiss()
            pytest.fail(f"XSS alert appeared: {alert.text}")
        except (NoAlertPresentException, UnexpectedAlertPresentException):
            pass

    def test_FR_001_028_sql_injection_email(self, driver):
        """' OR 1=1 -- in email must be rejected by the email validator."""
        tc = _tc("FR_001_028")
        page = RegisterPage(driver).open()
        page.fill_and_blur("email", tc["email"])
        err = page.field_error("email")
        assert err is not None, "Expected validation error for SQL-like email"
        assert "Email không hợp lệ" in err
