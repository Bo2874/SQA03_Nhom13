# Section 2 — Register flow (FR_001_006 to FR_001_009)
# =============================================================================

class TestRegisterFlow:
    def test_FR_001_006_register_success(
        self, driver, db_conn, redis_conn, cleanup_test_users
    ):
        tc = _tc("FR_001_006")
        email = _unique_email()
        page = RegisterPage(driver).open()
        page.fill_form(
            email=email,
            fullName=tc["fullname"],
            phone=tc["phone"],
            password=tc["password"],
            confirmPassword=tc["confirm_password"],
        )
        page.click_submit()
        assert page.is_in_otp_step(), "Form did not transition to OTP step"

        otp = wait_for_otp(redis_conn, email, timeout=10)
        page.fill_otp(otp)
        page.click_verify()

        page.wait_for_url_contains("/login", timeout=15)
        assert "Đăng ký tài khoản" in driver.page_source

        user = find_user_by_email(db_conn, email)
        assert user is not None, "User row was not created"
        assert user["full_name"] == tc["fullname"]
        assert user["role"] == "STUDENT"
        assert user["status"] == "ACTIVE"
        assert user["password_hash"].startswith("$2"), "Password not bcrypt-hashed"
        assert user["password_hash"] != tc["password"]

    def test_FR_001_007_email_already_exists(
        self, driver, db_conn, redis_conn, cleanup_test_users
    ):
        tc = _tc("FR_001_007")
        email = _unique_email(domain="gmail.com")
        # Seed an existing user with the same email
        execute(
            db_conn,
            "INSERT INTO users (email, password_hash, full_name, role, status) "
            "VALUES (%s, %s, %s, %s, %s)",
            (email, "$2b$10$dummyhashforexistingusertest123456789012345678901234", "Existing", "STUDENT", "ACTIVE"),
        )

        page = RegisterPage(driver).open()
        page.fill_form(
            email=email,
            fullName=tc["fullname"],
            phone=tc["phone"],
            password=tc["password"],
            confirmPassword=tc["confirm_password"],
        )
        page.click_submit()
        # OTP request still goes through (backend doesn't pre-check uniqueness)
        assert page.is_in_otp_step()
        otp = wait_for_otp(redis_conn, email, timeout=10)
        page.fill_otp(otp)
        page.click_verify()

        # Backend rejects with 409 — message should surface in toast or inline
        time.sleep(2)
        assert "already exists" in driver.page_source.lower() or "tồn tại" in driver.page_source

        # DB still has only the seeded row, no duplicate
        rows = fetch_one(
            db_conn,
            "SELECT COUNT(*) AS c FROM users WHERE email = %s",
            (email,),
        )
        assert rows["c"] == 1

    def test_FR_001_008_empty_form_submit(self, driver):
        page = RegisterPage(driver).open()
        page.click_submit()
        # yup runs onSubmit, errors render synchronously; small buffer for paint
        time.sleep(0.5)
        assert page.field_error("email") == "Vui lòng nhập email"
        assert page.field_error("fullName") == "Họ và tên là bắt buộc"
        assert page.field_error("password") == "Vui lòng nhập mật khẩu"
        assert page.field_error("confirmPassword") == "Vui lòng nhập lại mật khẩu"

    def test_FR_001_009_submit_debounce(
        self, driver, db_conn, redis_conn, cleanup_test_users
    ):
        tc = _tc("FR_001_009")
        email = _unique_email()
        page = RegisterPage(driver).open()
        page.fill_form(
            email=email,
            fullName=tc["fullname"],
            phone=tc["phone"],
            password=tc["password"],
            confirmPassword=tc["confirm_password"],
        )
        page.rapid_click_submit(n=5)
        # If only one click made it through, the form will already be on the
        # OTP step (proof of debounce). Otherwise the submit button must be
        # disabled or its label must have switched to "Đang gửi OTP...".
        if page.is_in_otp_step(timeout=5):
            return
        assert page.submit_disabled() or page.submit_text() != "Tiếp tục", (
            "Submit button did not debounce after rapid clicks"
        )


