"""FR_01 — User Registration system tests.

28 test cases sourced from sheet FR_01 in ss_test_13.xlsx
(see data/FR_01_user_registration.csv).

Conventions:
  - All emails generated by tests use settings.TEST_EMAIL_PREFIX so the
    cleanup_test_users fixture wipes only test data, not real users.
  - OTPs are read straight from Redis (key "otp:<email>") via
    utils.otp_helper.wait_for_otp — no real mailbox needed.
"""
